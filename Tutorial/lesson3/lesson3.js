// if else

let score1 = 80;

if(score1 === 81) {
    console.log("You passed bro!");
}else{
    console.log("Unfourtantely you failed!");
}

// switch case

let score2 = 85;

switch (true) {
    case (score2 === 80):
        console.log("Ok");
        break;
    default:
        console.log("No");
        break;
}

// Ternary operator

let score3 = 78;

score3 === 80 ? console.log("Ok") : console.log("No");